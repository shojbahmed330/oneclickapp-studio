window.calculateZakat = () => {
    const cash = parseFloat(document.getElementById('z-cash').value) || 0;
    const gold = parseFloat(document.getElementById('z-gold').value) || 0;
    const debt = parseFloat(document.getElementById('z-debt').value) || 0;
    const net = (cash + gold) - debt;
    const zakat = net > 0 ? (net * 0.025).toFixed(2) : 0;
    
    document.getElementById('z-amount').innerText = zakat;
    document.getElementById('z-result').classList.remove('hidden');
};


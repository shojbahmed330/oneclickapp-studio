const ApiService = {
    getPrayerTimes: async (city, country) => {
        try {
            const defaultTimes = {
                Fajr: "04:45", Dhuhr: "12:15", Asr: "16:30", Maghrib: "18:05", Isha: "19:30",
                Sunrise: "06:00", Sunset: "18:05"
            };
            const response = await fetch(`https://api.aladhan.com/v1/timingsByCity?city=${city}&country=${country}&method=1`);
            const data = await response.json();
            if(data.code === 200) return data.data.timings;
            return defaultTimes;
        } catch (e) {
            console.error('API Fail', e);
            return { Fajr: "04:45", Dhuhr: "12:15", Asr: "16:30", Maghrib: "18:05", Isha: "19:30", Sunrise: "06:00", Sunset: "18:05" };
        }
    }
};

const Utils = {
    vibrate: () => { if (window.navigator && window.navigator.vibrate) window.navigator.vibrate(50); },
    formatTime: (timeStr) => {
        if(!timeStr) return '--:--';
        let [h, m] = timeStr.split(':');
        let hour = parseInt(h);
        let ampm = hour >= 12 ? 'PM' : 'AM';
        hour = hour % 12;
        hour = hour ? hour : 12;
        return `${hour}:${m} ${ampm}`;
    },
    getRamadanDate: (dayIndex) => {
        let d = new Date(AppState.ramadanStart);
        d.setDate(d.getDate() + dayIndex);
        return d.toLocaleDateString('en-GB', { day: 'numeric', month: 'short' });
    },
    getRamadanTimes: (dayIndex) => {
        let fajrMin = 10 - dayIndex;
        let maghribMin = 0 + dayIndex;
        let sehriH = 5, sehriM = fajrMin - 15;
        if(sehriM < 0) { sehriH--; sehriM += 60; }
        let iftarH = 18, iftarM = maghribMin;
        const pad = (n) => n.toString().padStart(2, '0');
        return {
            sehri: `${pad(sehriH)}:${pad(sehriM)} AM`,
            iftar: `${pad(iftarH)}:${pad(iftarM)} PM`
        };
    },
    getHijriDate: () => {
        const today = new Date();
        const options = { year: 'numeric', month: 'long', day: 'numeric', calendar: 'islamic-umalqura' };
        try {
             return new Intl.DateTimeFormat('en-US-u-ca-islamic', options).format(today);
        } catch (e) {
            return "15 Shaban 1447"; // Fallback
        }
    }
};


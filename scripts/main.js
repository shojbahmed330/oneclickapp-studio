// --- AUDIO LOGIC ---
window.initAudio = (audioId, title) => {
    if (AppState.audioPlayer) {
        AppState.audioPlayer.pause();
    }
    
    const url = `https://server8.mp3quran.net/afs/${audioId}.mp3`;
    AppState.audioPlayer = new Audio(url);
    AppState.isPlaying = true;
    
    document.getElementById('mini-player').classList.remove('hidden');
    document.getElementById('player-title').innerText = title;
    document.getElementById('player-icon').className = "fas fa-pause";
    
    AppState.audioPlayer.play();
    
    AppState.audioPlayer.ontimeupdate = () => {
        const pct = (AppState.audioPlayer.currentTime / AppState.audioPlayer.duration) * 100;
        document.getElementById('player-progress').style.width = `${pct}%`;
    };
    
    AppState.audioPlayer.onended = () => {
        window.stopAudio();
    };
};

window.toggleAudio = () => {
    if(!AppState.audioPlayer) return;
    const icon = document.getElementById('player-icon');
    if(AppState.isPlaying) {
        AppState.audioPlayer.pause();
        AppState.isPlaying = false;
        icon.className = "fas fa-play";
    } else {
        AppState.audioPlayer.play();
        AppState.isPlaying = true;
        icon.className = "fas fa-pause";
    }
};

window.stopAudio = () => {
    if(AppState.audioPlayer) {
        AppState.audioPlayer.pause();
        AppState.audioPlayer.currentTime = 0;
    }
    AppState.isPlaying = false;
    document.getElementById('mini-player').classList.add('hidden');
};

